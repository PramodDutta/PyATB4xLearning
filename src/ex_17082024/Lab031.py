# x//y
# x%y
# x**y
# ==

print(5 // 2)  # ? Quotient
print(5 % 2)  # ? Remainder

# 2 | 5 | 2 - Quotient
#   | 4 |
# --------
#     1 - Remainder

print(13 // 2)
print(13 % 2)

# // and /
# / -> div
# // -> Quotient

print(2 ** 2)  # ** - power
print(2 ** 3)  # ** - power
print(2 ** 4)  # ** - power

#  ==  Compare Operator ( True or False

print(2 == 2)
print(2 == 3)
